def normalize_language(value: str | None) -> Language:
    return "en" if value == "en" else "zh"


@dataclass(frozen=True)
class I18n:
    language: Language = "zh"

    def t(self, key: str, fallback: str | None = None) -> str:
        return TRANSLATIONS[self.language].get(key, fallback if fallback is not None else key)

    def enum(self, prefix: str, value: object) -> str:
        raw = str(value)
        return self.t(f"{prefix}.{raw}", raw)
